    public static String escapeJava(String s) {
        StringBuilder buff = new StringBuilder();
        for (char c : s.toCharArray()) {
            if (c == '\\') {
                buff.append("\\\\");
            } else if (c == '\"') {
                buff.append("\\\"");
            } else {
                buff.append(c);
            }
        }
        return buff.toString();
    }

